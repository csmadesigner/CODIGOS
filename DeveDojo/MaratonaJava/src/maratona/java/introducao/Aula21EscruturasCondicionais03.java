package maratona.java.introducao;

public class Aula21EscruturasCondicionais03 {
    public static void main(String[] args) {
        /**
         * condições ternaria
         * condição  ? :  
         * ex : 
         *    10 > 11 ? se sim : se não  ; 
         *  */   
        int numero = 10 ; 
        String Result = numero == 10 ? "sim esse numero e 10" : "não, esse numero não é 10" ; 
        System.out.println(Result);

        boolean Result2 =  numero == 10 ? true : false ; 
        System.out.println(Result2);
    }

}
